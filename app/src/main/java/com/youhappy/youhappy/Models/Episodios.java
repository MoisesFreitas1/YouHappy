public class Episodios {
    private String Eid;
    private String Elink;
    private String Efigure;
    private String Eserie;
    private String Etitle;
    private String Esinopse;

    public Episodios() {
    }

    public String getEid() {
        return Eid;
    }

    public void setEid(String eid) {
        Eid = eid;
    }

    public String getElink() {
        return Elink;
    }

    public void setElink(String elink) {
        Elink = elink;
    }

    public String getEfigure() {
        return Efigure;
    }

    public void setEfigure(String efigure) {
        Efigure = efigure;
    }

    public String getEtitle() {
        return Etitle;
    }

    public void setEtitle(String etitle) {
        Etitle = etitle;
    }

    public String getEsinopse() {
        return Esinopse;
    }

    public void setEsinopse(String esinopse) {
        Esinopse = esinopse;
    }

    public String getEserie() {
        return Eserie;
    }

    public void setEserie(String eserie) {
        Eserie = eserie;
    }
}
